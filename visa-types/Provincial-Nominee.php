                </div>
                <h3 style="color: var(--color-burgundy); margin-bottom: 20px; text-align: center;">Express Entry-Linked PNP</h3>
                <p>These streams are aligned with the federal Express Entry system. If you receive a provincial nomination through an Express Entry-linked stream:</p>
                <ol style="margin-top: 15px;">
                    <li>Create an Express Entry profile</li>
                    <li>Receive a provincial nomination</li>
                    <li>Get 600 additional CRS points</li>
                    <li>Receive an Invitation to Apply (ITA) for permanent residence</li>
                    <li>Submit your permanent residence application to IRCC</li>
                </ol>
                <p style="margin-top: 15px;"><strong>Processing Time:</strong> Approximately 6-8 months after receiving an ITA</p>
            </div>
            
            <!-- Base PNP Streams -->
            <div class="process-card" style="background-color: var(--color-cream); padding: 30px; border-radius: 10px; box-shadow: 0 5px 15px rgba(0,0,0,0.05);">
