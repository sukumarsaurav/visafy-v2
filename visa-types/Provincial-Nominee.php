                    <i class="fas fa-file-alt" style="font-size: 1.5rem;"></i>
                </div>
                <h3 style="color: var(--color-burgundy); margin-bottom: 20px; text-align: center;">Base PNP Streams</h3>
                <p>These streams operate outside the Express Entry system with their own application process:</p>
                <ol style="margin-top: 15px;">
                    <li>Apply directly to the province for nomination</li>
                    <li>Receive a provincial nomination certificate</li>
                    <li>Submit a paper-based application for permanent residence to IRCC</li>
                </ol>
                <p style="margin-top: 15px;"><strong>Processing Time:</strong> Approximately 15-19 months after receiving nomination</p>
            </div>
