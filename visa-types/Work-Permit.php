                <ul>
                    <li>At least 12 months of full-time (or equivalent part-time) skilled work experience in Canada within the last 3 years</li>
                    <li>The experience must be in a managerial, professional, or technical/skilled trade occupation (NOC 0, A, or B)</li>
                    <li>Meet language requirements (CLB 7 for NOC 0 or A jobs, CLB 5 for NOC B jobs)</li>
                    <li>Plan to live outside the province of Quebec</li>
                </ul>
                <p>The CEC is managed through the Express Entry system.</p>
            </div>
            
