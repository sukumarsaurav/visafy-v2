                        <p><strong>LMIA-based work permits:</strong></p>
                        <ul>
                            <li>Require the employer to obtain a Labour Market Impact Assessment (LMIA) before hiring a foreign worker</li>
                            <li>The LMIA process verifies that no Canadian workers are available to fill the position</li>
                            <li>The employer must advertise the position and demonstrate efforts to hire Canadians</li>
                            <li>Typically have longer processing times due to the LMIA requirement</li>
                        </ul>
                        <p><strong>LMIA-exempt work permits:</strong></p>
                        <ul>
                            <li>Do not require an LMIA</li>
                            <li>Based on international agreements, Canadian interests, or specific programs</li>
                            <li>Examples include intra-company transferees, CUSMA/NAFTA professionals, and post-graduation work permits</li>
                            <li>Often have faster processing times</li>
                        </ul>
                        <p>Both types of work permits allow you to work legally in Canada, but the application requirements and processes differ significantly.</p>
                    </div>
                </div>
            </div>
            
